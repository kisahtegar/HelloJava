package com.dicoding.javafundamental.perulangan;

public class For {
    public static void main(String[] args) {
        for (int i = 1; i <= 10; i++) {
            System.out.println("Angka : " + i);
        }
    }
}

//output:

//Angka : 1
//Angka : 2
//Angka : 3
//Angka : 4
//Angka : 5
//Angka : 6
//Angka : 7
//Angka : 8
//Angka : 9
//Angka : 10